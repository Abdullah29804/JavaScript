// Variables declared by using var.
// prompt is used to get input from the user.
// alert() display message in a dialog box that pops up on the screen.
// console.log() outputs message to the console.
// document.write() outputs message to HTML web pages (Browser Window).
// To create a line break in JavaScript, use "<br>".


var a = prompt("Enter your name:");
alert(a);
document.write(a + "<br>");
console.log(a);
var question = "What is your age?";
var age = prompt(question);
alert(age);
document.write(age);
console.log(age);